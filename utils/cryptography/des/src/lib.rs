fn expand_key(key: u64) -> [u64; 16] {
    let permuted = permute(key, 64, &PC1);
    let (mut left, mut right) = split(permuted, 28);
    let mut keys = [0u64; 16];

    for i in 0..16 {
        let shift = if i == 0 || i == 1 || i == 8 || i == 15 {
            1
        } else {
            2
        };

        left = ((left << shift) | (left >> (28 - shift))) & 0xFFFFFFF;
        right = ((right << shift) | (right >> (28 - shift))) & 0xFFFFFFF;

        let combined = ((left as u64) << 28) | right as u64;
        keys[i] = permute(combined, 56, &PC2);
    }

    keys
}

fn permute<T: Bit, U: SetBit + Zero, const N: usize>(
    input: T,
    input_bits: u8,
    table: &[u8; N],
) -> U {
    let mut permuted = U::zero();
    let output_bits = table.len();

    for (i, &pos) in table.iter().enumerate() {
        let bit = input.bit((input_bits - pos) as usize);
        permuted.set_bit(output_bits - 1 - i, bit);
    }

    permuted
}

fn split(n: u64, size: u8) -> (u32, u32) {
    let left = (n >> size) as u32;
    let right = (n & ((1 << size) - 1)) as u32;

    (left, right)
}

fn substitute(input: u64) -> u32 {
    let mut substituted = 0;
    let mut bits = input;

    for i in 0..8 {
        let chunk = (bits & 0x3F) as u8;
        let sbox = S[8 - i - 1];
        let row = ((chunk.bit(5) as usize) << 1) | (chunk.bit(0) as usize);
        let col = ((chunk >> 1) & 0x0F) as usize;
        let substitution = sbox[row][col];
        substituted |= (substitution as u32) << (4 * i);
        bits >>= 6;
    }

    substituted
}

pub fn encrypt(block: u64, key: u64) -> u64 {
    let initial_permutation = permute(block, 64, &IP);
    let keys = expand_key(key);

    let (mut left, mut right) = split(initial_permutation, 32);

    for i in 0..16 {
        let expanded: u64 = permute(right, 32, &E);
        let combined = expanded ^ keys[i];
        let substituted = substitute(combined);
        let permuted: u32 = permute(substituted, 32, &P);
        let mixed = permuted ^ left;

        left = right;
        right = mixed;
    }

    let combined = ((right as u64) << 32) | (left as u64);
    permute(combined, 64, &IP_INV)
}

